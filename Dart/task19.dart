void main()
{
  //Write a program to print the 1 to 10 using For loop.
  for (var i = 1; i <= 10; i++)
  {
    print(i);    
  }
  print("");

  // Write a Program to print the 51 to 60 using while loop
  int i=51;
  while (i<=60)
  {
    print(i);
    i++;    
  }

  print("");
  // Write a program to print the 100 to 81 using do while loop.
  i=100;
  do {
    print(i);
    i--;
  } while (i>=81);
}